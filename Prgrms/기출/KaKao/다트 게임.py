"""
다트 게임은 총 3번의 기회로 구성된다.
각 기회마다 얻을 수 있는 점수는 0점에서 10점까지이다.
점수와 함께 Single(S), Double(D), Triple(T) 영역 : 점수에서 1제곱, 2제곱, 3제곱 (점수1 , 점수2 , 점수3 )으로 계산된다.
옵션으로 스타상(*) , 아차상(#)이 존재하며 스타상(*) 당첨 시 해당 점수와 바로 전에 얻은 점수를 각 2배로 만든다. 아차상(#) 당첨 시 해당 점수는 마이너스된다.
스타상(*)은 첫 번째 기회에서도 나올 수 있다. 이 경우 첫 번째 스타상(*)의 점수만 2배가 된다. (예제 4번 참고)
스타상(*)의 효과는 다른 스타상(*)의 효과와 중첩될 수 있다. 이 경우 중첩된 스타상(*) 점수는 4배가 된다. (예제 4번 참고)
스타상(*)의 효과는 아차상(#)의 효과와 중첩될 수 있다. 이 경우 중첩된 아차상(#)의 점수는 -2배가 된다. (예제 5번 참고)
Single(S), Double(D), Triple(T)은 점수마다 하나씩 존재한다.
스타상(*), 아차상(#)은 점수마다 둘 중 하나만 존재할 수 있으며, 존재하지 않을 수도 있다.
"""

# eval 함수 활용
def solution(dartResult):
    estimate = []
    for s in dartResult:
        if s.isdigit():
            if estimate and estimate[-1] == '+1':
                estimate[-1] = '+10'
                continue
            elif estimate and estimate[-1] == '1':
                estimate[-1] = '10'
                continue
            estimate.append('+'+s if estimate else s)
        elif s in ('S','D','T'):
            if s == 'S':
                estimate.append('**1')
            elif s == 'D':
                estimate.append('**2')
            else:
                estimate.append('**3')
        else:
            if s == '#':
                estimate.append('*(-1)')
            else:
                if len(estimate) <= 2:
                    estimate.append('*2')
                else:
                    estimate = estimate[:-2] + ['*2'] + estimate[-2:] + ['*2']
    # print(estimate)
    return eval(''.join(estimate))

print(solution('1D#2S*3S')) #5
print(solution("1D2S#10S")) #9

# 정규식 활용
import re
def solution(dartResult):
    bonus = {'S' : 1, 'D' : 2, 'T' : 3}
    option = {'' : 1, '*' : 2, '#' : -1}
    p = re.compile('(\d+)([SDT])([*#]?)')
    dart = p.findall(dartResult)
    for i in range(len(dart)):
        if dart[i][2] == '*' and i > 0:
            dart[i-1] *= 2
        dart[i] = int(dart[i][0]) ** bonus[dart[i][1]] * option[dart[i][2]]

    answer = sum(dart)
    return answer

